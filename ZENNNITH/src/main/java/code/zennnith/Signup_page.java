    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //get txt of the entryfield//
        String name = jTextField1.getText();
        String email = jTextField3.getText();
        String password = jTextField4.getText();
        
        //if empty it error//
        if (name.isEmpty() || email.isEmpty() || password.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill out all fields.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        //try catch or errorhandling//
        try {
            FileWriter fileWriter = new FileWriter("Admin.txt", true);
            BufferedWriter writer = new BufferedWriter(fileWriter);

            writer.write(name + "," + email + "," + password + "\n");
            writer.close();

            javax.swing.JOptionPane.showMessageDialog(this, "Account created successfully!");

        } catch (IOException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error creating account.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

